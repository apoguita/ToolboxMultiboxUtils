int CustomSkillClass::GetPtrBySkillID(GW::Constants::SkillID SkillID) {
    int Skillptr = 0;
    int ptr = 0;
    
    while ((Skillptr == 0) && (ptr < GetMaxSkillData()))
    {
        if (Skills[ptr].SkillID == SkillID) {
            Skillptr = ptr;
        }
        ptr++;
    }
    if (!Skillptr) { return 0; }
    return Skillptr;
}

void CustomSkillClass::Init(std::wstring file_dest) {

    // SERIALIZE SKILL FILE
    wchar_t path[MAX_PATH];
    const auto result = GetModuleFileNameW(plugin_handle, path, MAX_PATH);

    PathRemoveFileSpecW(path);

    std::wstring full_path = path;
    full_path += L"\\" + file_dest;

    std::string file_dest_mb(full_path.begin(), full_path.end());

    if (!fs::exists(full_path)) {
        InitSkillData();

        serializeSpecialSkills(file_dest_mb);
    }
    else {
        deserializeSpecialSkills(file_dest_mb);
    }

}

