void CustomSkillClass::serializeSpecialSkills(const std::string& filename) {
    json j = Skills;
    std::ofstream file(filename);
    if (file.is_open()) {
        file << j.dump(4);  // Pretty print with an indent of 4 spaces
        file.close();
    }
    else {
        //std::cerr << "Unable to open file for writing: " << filename << std::endl;
        WriteChat(GW::Chat::CHANNEL_GWCA1, L"Unable to open file for writing skills.json", L"HeroAI");
    }
}

void CustomSkillClass::deserializeSpecialSkills(const std::string& filename) {

    std::ifstream file(filename);
    if (file.is_open()) {
        json j;
        file >> j;
        file.close();
        j.get_to(Skills);
    }
    else {
        //std::cerr << "Unable to open file for reading: " << filename << std::endl;
        WriteChat(GW::Chat::CHANNEL_GWCA1, L"Unable to open file for reading skills.json", L"HeroAI");

    }
}

