class NameClass {
public:
    struct NameStruct {
        std::string Name;
    };

    NameClass() {
        Initialize();
    }
    void Initialize();

    /////////////// sKILLS //////////////
    std::string GetSkillNameByID(GW::Constants::SkillID skillID) {
        int ID = static_cast<int>(skillID);
        return Skills[ID].Name;
    }

    GW::Constants::SkillID GetSkillIDByName(const std::string& Name) {
        for (int i = 0; i < 3435; ++i) {
            if (Skills[i].Name == Name) {
                return static_cast<GW::Constants::SkillID>(i);
            }
        }
        return GW::Constants::SkillID::No_Skill; // Default if not found
    }

    ///////////// TYPE
    std::string GetTypeByID(GW::Constants::SkillType typeID) {
        int ID = static_cast<int>(typeID);
        return SkillTypes[ID].Name;
    }

    GW::Constants::SkillType GetIDByType(const std::string& Name) {
        for (int i = 0; i < 30; ++i) {
            if (SkillTypes[i].Name == Name) {
                return static_cast<GW::Constants::SkillType>(i);
            }
        }
        return GW::Constants::SkillType::Attack; // Default if not found
    }

    //////////////NATURE
    std::string GetNatureByID(SkillNature natureID) {
        int ID = static_cast<int>(natureID);
        return SSkillNature[ID].Name;
    }

    SkillNature GetIDByNature(const std::string& Name) {
        for (int i = 0; i < 15; ++i) {
            if (SSkillNature[i].Name == Name) {
                return static_cast<SkillNature>(i);
            }
        }
        return Offensive; // Default if not found
    }

    //////////////TARGET
    std::string GetTargetByID(SkillTarget targetID) {
        int ID = static_cast<int>(targetID);
        return SSkillTarget[ID].Name;
    }

    SkillTarget GetIDByTarget(const std::string& Name) {
        for (int i = 0; i < 20; ++i) {
            if (SSkillTarget[i].Name == Name) {
                return static_cast<SkillTarget>(i);
            }
        }
        return Enemy; // Default if not found
    }

    NameStruct Skills[3500];
    NameStruct SkillTypes[30];
    NameStruct SSkillTarget[20];
    NameStruct SSkillNature[15];



    
};

